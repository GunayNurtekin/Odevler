using System;

namespace Soru6;

public class ScientificCalculator : Calculator
{
    public double Sin(double angel)
    {
        return Math.Sin(angel);
    } 

     public double Cos(double angel)
    {
        return Math.Cos(angel);
    } 

     public double Tan(double angel)
    {
        return Math.Tan(angel);
    } 

}
